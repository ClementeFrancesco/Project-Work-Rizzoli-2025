package it.itsrizzoli.prjw;

public class Veicolo {
    String targa;
    String marca;
    String modello;
    int id_veicolo;

    public Veicolo(String targa, String marca, String modello, int id_veicolo) {
        this.targa = targa;
        this.marca = marca;
        this.modello = modello;
        this.id_veicolo = id_veicolo;
    }
}